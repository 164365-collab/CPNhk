def คำนวณพื้นที่(กว้าง, ยาว):
    return กว้าง * ยาว

def คำนวณเส้นรอบรูป(กว้าง, ยาว):
    return 2 * (กว้าง + ยาว)

try:
    กว้าง = float(input("กรุณาใส่ความกว้าง: "))
    ยาว = float(input("กรุณาใส่ความยาว: "))

    if กว้าง <= 0 or ยาว <= 0:
        print("ความกว้างและความยาวต้องมากกว่า 0")
    else:
        พื้นที่ = คำนวณพื้นที่(กว้าง, ยาว)
        เส้นรอบรูป = คำนวณเส้นรอบรูป(กว้าง, ยาว)

        print("\nผลลัพธ์")
        print("-" * 20)
        print(f"ความกว้าง   : {กว้าง}")
        print(f"ความยาว     : {ยาว}")
        print(f"พื้นที่      : {พื้นที่:.2f}")
        print(f"เส้นรอบรูป  : {เส้นรอบรูป:.2f}")

except ValueError:
    print("กรุณาป้อนตัวเลขที่ถูกต้อง")